function show_login() {
    var obj = document.getElementById("login");
    var obk = document.getElementById("daftar");
    obj.style.display = "block";
    obk.style.display = "none";
};

function show_daftar() {
    var obj = document.getElementById("login");
    var obk = document.getElementById("daftar");
    obj.style.display = "none";
    obk.style.display = "block";
}